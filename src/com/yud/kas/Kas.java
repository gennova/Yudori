package com.yud.kas;

/**
 *
 * @author legollas
 */
public class Kas {

    private int idKas;
    private String kodeKas;
    private String kodeManKas;
    private String tanggal;
    private String uraian;
    private String jenisKas;
    private int saldo;
    private String kodeakun;
    private String kasMasuk;
    private String kasKeluar;

    public int getIdKas() {
        return idKas;
    }

    public void setIdKas(int idKas) {
        this.idKas = idKas;
    }

    public String getKodeKas() {
        return kodeKas;
    }

    public void setKodeKas(String kodeKas) {
        this.kodeKas = kodeKas;
    }

    public String getKodeManKas() {
        return kodeManKas;
    }

    public void setKodeManKas(String kodeManKas) {
        this.kodeManKas = kodeManKas;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getUraian() {
        return uraian;
    }

    public void setUraian(String uraian) {
        this.uraian = uraian;
    }

    public String getJenisKas() {
        return jenisKas;
    }

    public void setJenisKas(String jenisKas) {
        this.jenisKas = jenisKas;
    }

    public int getSaldo() {
        return saldo;
    }

    public void setSaldo(int saldo) {
        this.saldo = saldo;
    }

    public String getKodeakun() {
        return kodeakun;
    }

    public void setKodeakun(String kodeakun) {
        this.kodeakun = kodeakun;
    }

    public String getKasMasuk() {
        return kasMasuk;
    }

    public void setKasMasuk(String kasMasuk) {
        this.kasMasuk = kasMasuk;
    }

    public String getKasKeluar() {
        return kasKeluar;
    }

    public void setKasKeluar(String kasKeluar) {
        this.kasKeluar = kasKeluar;
    }

}
