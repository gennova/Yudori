    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int wor = tabelBstuAll.getSelectedRow();
        if (wor >= 0) {
            String kode = tabelBstuAll.getValueAt(wor, 1).toString();
            txtNoFaktur.setText(kode);
            DaoFactory.getSerahTerimaUangDao().truncateTempBSTU();
            SerahTerimaUang stu = DaoFactory.getSerahTerimaUangDao().getSerahTerimaByKode(kode);
            txtNama.setText(stu.getNama());
            txtDiserahkan.setText(stu.getDiserahkan());
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            try {
                tglBSTU.setDate(df.parse(stu.getTanggal()));
            } catch (ParseException ex) {
                Logger.getLogger(BSTUang.class.getName()).log(Level.SEVERE, null, ex);
            }
            String status = stu.getStatus();
            if (status.equalsIgnoreCase("masuk")) {
                cekMasuk.setSelected(true);
            } else if (status.equalsIgnoreCase("keluar")) {
                cekKeluar.setSelected(true);
            }
            String jenisnya = stu.getJenisbstu();
            if (jenisnya.equalsIgnoreCase("Angsuran")) {
                radAngsuran.setSelected(true);
            } else if (jenisnya.equalsIgnoreCase("Tunai")) {
                radTunai.setSelected(true);
            } else if (jenisnya.equalsIgnoreCase("Komisi")) {
                radKomisi.setSelected(true);
            } else if (jenisnya.equalsIgnoreCase("Lainnya")) {
                radLainnya.setSelected(true);
            }
            DaoFactory.getSerahTerimaUangDao().lookBSTUDetailToTemp(kode);
            jDesktopPane2.setVisible(false);
            jDesktopPane1.setVisible(true);
            showBSTUTemp();
        }
    }//GEN-LAST:event_jButton7ActionPerformed

