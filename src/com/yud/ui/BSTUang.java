    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            String kodefaktur = txtNoFaktur.getText();
            String namapenerima = txtNama.getText();
            String namapenyerah = txtDiserahkan.getText();
            String jumlah = txtTotalnya.getText();
            String uraian = txtUraian.getText();
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String tglnya = df.format(tglBSTU.getDate());
            String statuscek = "";
            if (cekMasuk.isSelected() == true) {
                statuscek = "Masuk";
            } else if (cekKeluar.isSelected() == true) {
                statuscek = "Keluar";
            }
            String jeniskeluar = "";
            if (radAngsuran.isSelected() == true) {
                jeniskeluar = "Angsuran";
            } else if (radKomisi.isSelected() == true) {
                jeniskeluar = "Komisi";
            } else if (radTunai.isSelected() == true) {
                jeniskeluar = "Tunai";
            } else if (radLainnya.isSelected() == true) {
                jeniskeluar = "Lainnya";
            }
            SerahTerimaUang stu = new SerahTerimaUang();
            stu.setKodeBSTU(kodefaktur);
            stu.setNama(namapenerima);//penerima
            stu.setDiserahkan(namapenyerah);
            stu.setJenisbstu(jeniskeluar);
            stu.setStatus(statuscek);
            stu.setJumlah(Integer.parseInt(jumlah));
            stu.setTanggal(tglnya);
            stu.setUraian(uraian);
            DaoFactory.getSerahTerimaUangDao().insertBSTU(stu);
            DaoFactory.insertUrutBSTU(txtNoFaktur.getText());
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            new PrintReport("./report/serahterimauang.jasper", "kodefaktur", kodefaktur);
            clearfield();
            DaoFactory.getSerahTerimaUangDao().truncateTempBSTU();
            loadform();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
            Logger.getLogger(BSTUang.class.getName()).log(Level.SEVERE, null, ex);
        }
        clearfield();
        DaoFactory.getSerahTerimaUangDao().truncateTempBSTU();
        loadform();
    }//GEN-LAST:event_jButton2ActionPerformed

