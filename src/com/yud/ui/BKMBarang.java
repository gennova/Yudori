    private void loadform() {
        tglKeluar.setDate(new GregorianCalendar().getTime());
        List<Barang> barangs = DaoFactory.getBarangDao().getAllBarang();    
        comboBarang.removeAllItems();
        for (int i = 0; i < barangs.size(); i++) {
            Barang b = barangs.get(i);
            comboBarang.addItem(b.getNamabarang());
        }
        cekKeluar.setSelected(true);
        txtNoFaktur.setText(DaoFactory.getIDUrutBKM());
        showbkm();
    }
