    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String kodekmb = txtkodekmb.getText();
        try {
        boolean status = new KeluarMasukBarang().cekkmb(kodekmb);
        if (status == false) {
            String noFaktur = txtNoFaktur.getText();
            String namapeminta = txtpeminta.getText();
            String alamat = txtalamat.getText();
            String statusbarang = "";
            if (cekKeluar.isSelected()) {
                statusbarang = "keluar";
            } else if (cekMasuk.isSelected()) {
                statusbarang = "masuk";
            }
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String tanggal = df.format(tglKeluar.getDate());
            String keterangan = txtketerangan.getText();
            Barang b = DaoFactory.getBarangDao().getBarangByName(comboBarang.getSelectedItem().toString());
            String unit = txtunit.getText();
            String keterangandetil = txtdetailketerangan.getText();
            KeluarMasukBarang barang = new KeluarMasukBarang();
            barang.setKodeKMB(noFaktur);
            barang.setNama(namapeminta);
            barang.setAlamat(alamat);
            barang.setStatusbarang(statusbarang);
            barang.setTanggal(tanggal);
            barang.setBarang(b);
            barang.setJumlah(Integer.parseInt(unit));
            barang.setKeterangan(keterangan);
            barang.setDetailketerangan(keterangandetil);
            DaoFactory.getKeluarMasukBarangDao().insertKMB(barang);
            try {
                DaoFactory.insertUrutBKM(noFaktur);
            } catch (SQLException ex) {
                Logger.getLogger(BKMBarang.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            new PrintReport("./report/bkmb.jasper", "kodefaktur", noFaktur);
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Kesalahan Data "+ e.getMessage());
        }
        clearfield();
    }//GEN-LAST:event_jButton4ActionPerformed

