    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String kodeand = txtkodeangsuran.getText();
        boolean status = new Angsuran().cekangsuran(kodeand);
        if (status == false) {
            if (!"".equals(txtkodebp.getText())) {
                DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                String kodebp = txtkodebp.getText();
                String pembayar = txtpembayar.getText();
                String kodeangsuran = txtkodefaktur.getText();
                JOptionPane.showMessageDialog(null, pembayar);
                Pelanggan p = DaoFactory.getPelangganDao().getPelangganByName(pembayar);
                String jumlahuang = txtjumlah.getText();
                String tglterima = df.format(tglterimadate.getDate());
                String keterangan = txtketerangan.getText();
                String telp = txttelp.getText();
                String penerima = txtpenerima.getText();
                Angsuran a = new Angsuran();
                a.setKodeBP(kodebp);
                a.setKodeAngsuran(kodeangsuran);
                a.setPelanggan(p);
                a.setPembayar(p.getNamaPelanggan());
                a.setJumlah(Integer.parseInt(jumlahuang));
                a.setPenerima(penerima);
                a.setTanggalTerima(tglterima);
                a.setKeterangan(keterangan);
                DaoFactory.getAngsuranDao().insertAngsuran(a);
                DaoFactory.getAngsuranDao().insertAngsuranBulananUM(a);
                try {
                    DaoFactory.insertUrutAngsuran(kodeangsuran);
                } catch (SQLException ex) {
                    Logger.getLogger(AngsuranUI.class.getName()).log(Level.SEVERE, null, ex);
                }
                clearfield();
                JOptionPane.showMessageDialog(null, "Angsuran Berhasil Disimpan");
                new PrintReport("./report/ans.jasper", "nobp", a.getKodeBP());
            }
        } else {
            JOptionPane.showMessageDialog(null, "data sudah ada");
        }

    }//GEN-LAST:event_jButton6ActionPerformed

