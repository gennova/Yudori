    private void tabelanksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelanksMouseClicked
        // TODO add your handling code here:
        int row = tabelanks.getSelectedRow();
        if (row >= 0) {
            String kodeans = tabelanks.getValueAt(row, 1).toString();
            String kodebp = tabelanks.getValueAt(row, 2).toString();
            String pembayar = tabelanks.getValueAt(row, 3).toString();
            String jumlah = tabelanks.getValueAt(row, 4).toString();
            String penerima = tabelanks.getValueAt(row, 5).toString();
            String tgl = tabelanks.getValueAt(row, 6).toString();
            txtkodeangsuran.setText(kodeans);
            txtkodebp.setText(kodebp);
            txtpembayar.setText(pembayar);
            txtjumlah.setText(jumlah);
            txtpenerima.setText(penerima);
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            try {
                tglterimadate.setDate(df.parse(tgl));
            } catch (ParseException ex) {
                Logger.getLogger(AngsuranUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tabelanksMouseClicked

