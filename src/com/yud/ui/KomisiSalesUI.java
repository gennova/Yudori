            txtrpkoma1.setText(String.valueOf(komisi.getRpkomisia1()));
            txtrpkomlunas.setText(String.valueOf(komisi.getRpkomisilunas()));
            txtrpkomtunai.setText(String.valueOf(komisi.getRpkomisitunai()));
