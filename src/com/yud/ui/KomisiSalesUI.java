    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String kombp = txtkombp.getText();
        String koma1 = txtkoma1.getText();
        String komlunas = txtkomlunas.getText();
        String komtunai = txtkomtunai.getText();
        String kodebp = txtkodebp.getText();
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String tanggalnya = df.format(new GregorianCalendar().getTime());
        double kombp1 = Double.parseDouble(kombp);
        double koma11 = Double.parseDouble(koma1);
        double komalunas1 = Double.parseDouble(komlunas);
        double komtunai1 = Double.parseDouble(komtunai);
        if (kombp1 > 0.0) {
            System.out.println("execute kombp");
            DaoFactory.getKomisiDao().postingupdatestatuskomisibp(tanggalnya, kodebp);
        }
        if (koma11 > 0.0) {
            System.out.println("execute koma1");
            DaoFactory.getKomisiDao().postingupdatestatuskomisia1(tanggalnya, kodebp);
        }
        if (komalunas1 > 0.0) {
            System.out.println("execute komlunas");
            DaoFactory.getKomisiDao().postingupdatestatuskomisilunas(tanggalnya, kodebp);
        }
        if (komtunai1 > 0.0) {
            System.out.println("execute komtunai");
            DaoFactory.getKomisiDao().postingupdatestatuskomisitunai(tanggalnya, kodebp);
        }
        Komisi komisi = new Komisi();
        komisi.setBp(DaoFactory.getBuktiPesananDao().getBuktiPesananByKodeBP(kodebp));
        komisi.setKomisibp(Double.parseDouble(kombp));
        komisi.setKomisia1(Double.parseDouble(koma1));
        komisi.setKomisilunas(Double.parseDouble(komlunas));
        komisi.setKomdisitunai(Double.parseDouble(komtunai));
        DaoFactory.getKomisiDao().updateKomisiSalesSaja(komisi);
    }//GEN-LAST:event_jButton5ActionPerformed

